
public class bitwiseOperator {

    public static void main(String[] args) {
        
        System.out.println(5&6);//bitwise AND
        System.out.println(5|6);//bitwise OR
        System.out.println(5^6);//bitwise XOR
        System.out.println(~5);//binary one`s compliment
        System.out.println(~0);
        System.out.println(5<<6);//Binary left shift
        System.out.println(5>>6);//Binary right shift
    }


    
}
